# Function to concatenate two strings
def concatenate_strings():
    # Take first input
    str1 = input("Enter the first string: ")
    # Take second input
    str2 = input("Enter the second string: ")
    # Concatenate strings
    result = str1 + str2
    # Print result
    print("The concatenated string is:", result)

# Call the function
concatenate_strings()